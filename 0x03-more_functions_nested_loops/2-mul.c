/**
 * mul - function that adds 2 integers
 * @a: is one of the integers added together
 * @b: is one of the integers added together
 *
 * Description: multiplies two integers
 * Return: The product of the 2 integers is returned
 */
int mul(int a, int b)
{
	int mult;

	mult = a * b;
	return (mult);
}
