#include "3-calc.h"

/**
 * op_add - function that gets the sum of  2 numbers
 * @a: the 1st number
 * @b: the 2nd number
 *
 * Return: the sum
 */

int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - function that gets the difference of  2 numbers
 * @a: the 1st number
 * @b: the 2nd number
 *
 * Return: the difference
 */

int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - function that gets the product of  2 numbers
 * @a: the 1st number
 * @b: the 2nd number
 *
 * Return: the product
 */

int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - function that divides 2 numbers
 * @a: the 1st number
 * @b: the 2nd number
 *
 * Return: the result of the divided number
 */

int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - function that gets the remainder of  2 numbers
 * @a: the 1st numner
 * @b: the 2nd number
 *
 * Return: the remainder
 */

int op_mod(int a, int b)
{
	return (a % b);
}
